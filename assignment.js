// = += -= *= /= %= ++ --

let x = 10;
x += 4; // x = x + 4
console.log(x);

let y = 10;
y -= 6; // y = y - 6
console.log(y);

let z = 10;
z *= 5; // z = z * 5
console.log(z);

let a = 10;
a /= 2; // a = a / 4
console.log(a);

let b = 10;
let res = b++; //res = 10 .. 11
console.log(res);

let m = 15;
let res1 = ++m; //res = 16 .. 16
console.log(res1);

let c = 10;
let res2 = --c;
console.log(res2);